  double _visualZoom = 1.0;
  Offset _visualCenter = const Offset(0.5, 0.5);

  @override
  void initState() {
    super.initState();

    _initialize();
  }

  void _initialize() async {
    if (_cameras.isEmpty) {
      _cameras = await availableCameras();
    }
    for (var i = 0; i < _cameras.length; i++) {
      if (_cameras[i].lensDirection == widget.initialCameraLensDirection) {
        _cameraIndex = i;
        break;
      }
    }
    if (_cameraIndex != -1) {
      _startLiveFeed();
    }
    // listen to face rect notifier if provided
    widget.faceRectNotifier?.addListener(() {
      final r = widget.faceRectNotifier!.value;
      if (r == null) {
        // reset target to full view
        _targetCenter = const Offset(0.5, 0.5);
        _targetZoom = 1.0;
      } else {
        // r is normalized (left, top, width, height) in image coords
        final center = Offset(r.left + r.width / 2, r.top + r.height / 2);
        _targetCenter = center;
        // desiredFacePortion controls how much of the viewport width the face should occupy
        const desiredFacePortion = 0.5; // face should take ~50% of view width
        // estimate target zoom from face width (normalized): targetZoom = desired / faceWidth
        var computed = (desiredFacePortion / (r.width.clamp(0.01, 1.0)));
        // clamp to camera zoom range if known
        computed = computed.clamp(_minAvailableZoom, _maxAvailableZoom);
        _targetZoom = computed;
      }
    });
  }

  @override
  void dispose() {
    _followTimer?.cancel();
    _stopLiveFeed();
    super.dispose();
  }

