  // representing the detected face bounding box (left, top, width, height).
  // FaceDetectorView will update this notifier. CameraView listens and
  // smoothly adjusts zoom/translation to follow that rect.
  final ValueNotifier<Rect?>? faceRectNotifier;
  final CameraLensDirection initialCameraLensDirection;

  @override
  State<CameraView> createState() => _CameraViewState();
}

